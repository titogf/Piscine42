#include <unistd.h>

void	ft_putstr(char *str);

int	main(int argc, char *argv[])
{
	argc = 1;
	ft_putstr(argv[0]);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write (1, str + i, 1);
		i++;
	}
	write (1, "\n", 1);
}
