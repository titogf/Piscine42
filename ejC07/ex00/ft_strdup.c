#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*p;

	i = 0;
	p = malloc(sizeof(src));
	if (p == 0)
		return (0);
	while (src[i] != '\0')
	{
		p[i] = src[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
