#include	<unistd.h>

void	ft_putnbr(int nb)
{
	int	a;
	int	b;
	
	if (nb < 0)
	{
		nb *= -1;
		write(1, "-", 1);
		write(1, &nb, 1);
	}
	else if (nb < 9)
	{
		nb += 48;
		write(1, &nb, 1);
	}
	else
	{
		a = (nb / 10)+48;
		b = (nb % 10)+48;
		write(1, &a, 1);
		write(1, &b, 1);
	}
}
